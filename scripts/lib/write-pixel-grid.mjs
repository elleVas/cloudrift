import { Jimp, rgbaToInt } from 'jimp';
import { writeFile } from 'fs/promises';

/**
 * Shared codegen core for generate-brand-mark-icon.mjs and
 * generate-brand-mark-title.mjs: samples a `width` x `rows*2` grid via
 * `colorAt(col, row)` (each script's own image-specific pre-processing —
 * posterizing real RGB for the icon, dilating + masking a monochrome
 * outline into a gradient for the title — happens before this runs) and
 * writes it as a `PixelArtCell` grid, in the exact shape brand-mark.ts's
 * `renderCell` already knows how to render.
 */
export async function writePixelGridData({ outputPath, exportName, sourceLabel, width, rows, colorAt, previewPath }) {
  const cells = [];
  for (let row = 0; row < rows; row++) {
    const line = [];
    for (let col = 0; col < width; col++) {
      line.push({ top: colorAt(col, row * 2), bottom: colorAt(col, row * 2 + 1) });
    }
    cells.push(line);
  }

  const body = cells
    .map(
      (line) =>
        '  [' +
        line
          .map((c) => `{ top: ${c.top ? `'${c.top}'` : 'null'}, bottom: ${c.bottom ? `'${c.bottom}'` : 'null'} }`)
          .join(', ') +
        '],',
    )
    .join('\n');

  const output = `// SPDX-License-Identifier: Apache-2.0
// Generated by scripts/generate-brand-mark-*.mjs from ${sourceLabel} — do not hand-edit.
// Re-run that script after changing the source image.
import type { PixelArtCell } from './pixel-art-cell';

export const ${exportName}: readonly (readonly PixelArtCell[])[] = [
${body}
];
`;

  await writeFile(outputPath, output);
  console.log(`Wrote ${outputPath} (${width}x${rows} cells from ${sourceLabel})`);

  if (previewPath) {
    const scale = 20;
    const preview = new Jimp({ width: width * scale, height: rows * 2 * scale });
    for (let row = 0; row < rows; row++) {
      for (let col = 0; col < width; col++) {
        const { top, bottom } = cells[row][col];
        for (let subRow = 0; subRow < 2; subRow++) {
          const hex = subRow === 0 ? top : bottom;
          const color = hex ? rgbaToInt(...hexToRgb(hex), 255) : rgbaToInt(255, 255, 255, 0);
          for (let py = 0; py < scale; py++) {
            for (let px = 0; px < scale; px++) {
              preview.setPixelColor(color, col * scale + px, (row * 2 + subRow) * scale + py);
            }
          }
        }
      }
    }
    await preview.write(previewPath);
    console.log(`Wrote preview ${previewPath}`);
  }

  return cells;
}

export function hexToRgb(hex) {
  const n = parseInt(hex.slice(1), 16);
  return [(n >> 16) & 255, (n >> 8) & 255, n & 255];
}

export function toHex({ r, g, b }) {
  return '#' + [r, g, b].map((c) => c.toString(16).padStart(2, '0')).join('');
}

export function lerpHex(a, b, t) {
  const [ar, ag, ab] = hexToRgb(a);
  const [br, bg, bb] = hexToRgb(b);
  const mix = (x, y) => Math.round(x + (y - x) * t).toString(16).padStart(2, '0');
  return `#${mix(ar, br)}${mix(ag, bg)}${mix(ab, bb)}`;
}
