// One-off/dev codegen: base64-embeds docs/assets/cloudrift.png into
// apps/cli/src/pdf-logo-data.ts so the PDF masthead can draw the real logo
// without depending on a file path that survives npm packaging (the CLI
// ships as a single esbuild bundle — a relative path to docs/assets/ from
// the installed package wouldn't resolve). Re-run after changing the logo.
import { readFile, writeFile } from 'fs/promises';

const SOURCE = 'docs/assets/cloudrift.png';
const OUTPUT = 'apps/cli/src/pdf-logo-data.ts';

const buffer = await readFile(SOURCE);
const base64 = buffer.toString('base64');

const output = `// SPDX-License-Identifier: Apache-2.0
// Generated by scripts/generate-pdf-logo-data.mjs from ${SOURCE} — do not hand-edit.
// Re-run that script after changing the source image.

export const PDF_LOGO_PNG_BASE64 =
  '${base64}';
`;

await writeFile(OUTPUT, output);
console.log(`Wrote ${OUTPUT} (${(buffer.length / 1024).toFixed(1)} KB source -> ${(base64.length / 1024).toFixed(1)} KB base64)`);
